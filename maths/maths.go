package maths

// Add takes 2 integers as input and return the sum
func Add(x, y int) (res int) {
	return x + y
}

// Subtract takes 2 integers as input and return the difference
func Subtract(x, y int) (res int) {
	return x - y
}

// Multiply takes 2 integers as input and return the product
func Multiply(x, y int) (res int) {
	return x * y
}

// Divide takes 2 integers as input and return the quotient
func Divide(x, y int) (res int) {
	return x / y
}
